if ($handle = opendir('templates')) {
	while (false !== ($filename = readdir($handle))) {
		if (mb_strpos($filename, '.') !== false && mb_substr($filename, mb_strrpos($filename, '.')) == '.mst') {
			$data['templates'][str_replace('.', '_', mb_substr($filename, 0, mb_strrpos($filename, '.')))] = file_get_contents('templates/'.$filename);
		}
	}
	closedir($handle);
}

$guild_ids = array_keys($data['guilds']);

foreach ($guild_ids as $guild_id) {
	$data['guilds'][$guild_id]['id'] = $guild_id;
	$player_ids = array_keys($data['guilds'][$guild_id]['players']);
	foreach ($player_ids as $player_id) {
		$data['guilds'][$guild_id]['players'][$player_id]['id'] = $player_id;
	}
}

$plot_ids = array_keys($data['plots']);

foreach ($plot_ids as $plot_id) {
	$data['plots'][$plot_id]['id'] = $plot_id;
}

$setting_ids = array_keys($data['settings']);

foreach ($setting_ids as $setting_id) {
	$data['settings'][$setting_id]['id'] = $setting_id;
}

$faq_ids = array_keys($data['faqs']);

foreach ($faq_ids as $faq_id) {
	$data['faqs'][$faq_id]['id'] = $faq_id;
}

uasort(
	$data['guilds'],
	function($guild_a, $guild_b) {
		return strcmp($guild_a['name'], $guild_b['name']);
	}
);

foreach ($guild_ids as $guild_id) {
	uasort(
		$data['guilds'][$guild_id]['players'],
		function($player_a, $player_b) {
			return strcmp($player_a['name'], $player_b['name']);
		}
	);
}

uasort(
	$data['plots'],
	function($plot_a, $plot_b) {
		return strcmp($plot_a['name'], $plot_b['name']);
	}
);

